package series;

public class A {
	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++){
			System.out.println(i);
		}
		
		for (int i=1; i<=10; i++) {
			System.out.println(i*i);
		}
		
		for (int i=1; i<=10; i++) {
			System.out.println(i*i*i);
		}
		
		
		int i=1;
		while(i<=10) {   // entry controlled
			System.out.println(i*i);
			i++;
		}
		
		
		
	}
	
	
}
