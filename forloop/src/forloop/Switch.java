package forloop;

public class Switch {
	public static void main(String[] args) {
		String a = "pooja";
		switch (a) {
		case "kiran":
			System.out.println("a is one");
			break;
		case "nilam":
			System.out.println("a is two");
			break;
		case "pooja":
			System.out.println("a is three");
			break;
		case "rutuja":
			System.out.println("a is ten");
			break;
			
		}
	}

}
